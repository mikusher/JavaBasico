    private void mostrarPesquisa(List<ContatosAluno> contatos) {

        while (tmContato.getRowCount() > 0) {
            tmContato.removeRow(0);
        }
        if (contatos.size() == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum contato cadastrado");
        } else {
            String[] linha = new String[] { null, null, null, null, null, null, null, null };
            for (int i = 0; i < contatos.size(); i++) {
                tmContato.addRow(linha);
                tmContato.setValueAt(contatos.get(i)
                                             .getId(),
                                     i,
                                     0);
                tmContato.setValueAt(contatos.get(i)
                                             .getNome(),
                                     i,
                                     1);
                tmContato.setValueAt(contatos.get(i)
                                             .getEndereco(),
                                     i,
                                     2);
                tmContato.setValueAt(contatos.get(i)
                                             .getEmail(),
                                     i,
                                     4);
                tmContato.setValueAt(contatos.get(i)
                                             .getTelefone(),
                                     i,
                                     3);
                tmContato.setValueAt(contatos.get(i)
                                             .getSexo(),
                                     i,
                                     5);
                tmContato.setValueAt(contatos.get(i)
                                             .getGrau(),
                                     i,
                                     6);
                tmContato.setValueAt(contatos.get(i)
                                             .getDisciplinas(),
                                     i,
                                     7);
            }

        }
    }





    private void alterarContatos() throws SQLException {

        if (jTable1.getSelectedRow() != -1) {
            if (verificaDados()) {
                ContatosAluno c1 = new ContatosAluno();
                ContatoAlunoCombina cc = new ContatoAlunoCombina();
                c1.setId(Long.valueOf(jTid.getText()));
                c1.setNome(jTnome.getText());
                c1.setEndereco(jTendereco.getText());
                c1.setTelefone(jTtelefone.getText());
                c1.setEmail(jTemail.getText());
                c1.setSexo(jTsexo.getText());
                c1.setGrau(jTgrau.getText());
                c1.setDisciplinas(jTdisciplinas.getText());
                cc.altera(c1);
                JOptionPane.showMessageDialog(null, "Alteração, com sucesso!");
            }
        }
    }
