    public boolean verificaDados() {

        if (!jTnome.getText()
                   .equals("")
            && !jTgrau.getText()
                      .equals("")) {

            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Campo de Nome ou Grau, a ser preenchidos");
        }
        return false;
    }





    private void tabelaSelecionado(JTable tabela) {

        if (jTable1.getSelectedRow() != -1) {
            habilitadata();
            jTid.setText(String.valueOf(contatos.get(tabela.getSelectedRow())
                                                .getId()));
            jTnome.setText(contatos.get(tabela.getSelectedRow())
                                   .getNome());
            jTendereco.setText(contatos.get(tabela.getSelectedRow())
                                       .getEndereco());
            jTemail.setText(contatos.get(tabela.getSelectedRow())
                                    .getEmail());
            jTtelefone.setText(contatos.get(tabela.getSelectedRow())
                                       .getTelefone());
            jTsexo.setText(contatos.get(tabela.getSelectedRow())
                                   .getSexo());
            jTgrau.setText(contatos.get(tabela.getSelectedRow())
                                   .getGrau());
            jTdisciplinas.setText(contatos.get(tabela.getSelectedRow())
                                          .getDisciplinas());
        } else {
            jTid.setText("");
            jTnome.setText("");
            jTendereco.setText("");
            jTtelefone.setText("");
            jTsexo.setText("");
            jTemail.setText("");
            jTgrau.setText("");
            jTdisciplinas.setText("");
        }
    }





