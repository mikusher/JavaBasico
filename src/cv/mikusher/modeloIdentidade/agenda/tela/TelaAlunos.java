    public void excluirCont() throws SQLException {

        int dica = JOptionPane.showConfirmDialog(this, "Deseja Apagar o Contato?", "Confirma!", JOptionPane.YES_NO_OPTION);
        if (dica == JOptionPane.YES_NO_OPTION) {
            ContatoAlunoCombina cc = new ContatoAlunoCombina();
            cc.remove(contatos.get(jTable1.getSelectedRow()));
        }
    }





    public void Guarda() {

        try {
            // TODO add your handling code here:
            ContatosAluno ct = new ContatosAluno();
            ct.setNome(jTnome.getText());
            ct.setEndereco(jTendereco.getText());
            ct.setEmail(jTemail.getText());
            ct.setTelefone(jTtelefone.getText());
            ct.setSexo(jTsexo.getText());
            ct.setGrau(jTgrau.getText());
            ct.setDisciplinas(jTdisciplinas.getText());

            ContatoAlunoCombina dao = new ContatoAlunoCombina();
            dao.adiciona(ct);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Problemas em Pesquisa");
        }
    }





