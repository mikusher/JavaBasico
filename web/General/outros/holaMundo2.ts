/**
 * Tipos em type script
 * string, boolean, array, number
 * variaveis usando o let ou var
 * 
 * Quando se o LET a minha variavel se torna limitada ao bloco em que esta sendo utilizada
 * Quando se o o VAR a variavel tona em ambito Global, sem limite.
 * let -> variavel local
 * var -> variavel global
 */

 let meuNumero: number = 50;
 let meuString: string = "Luis Tavares";
 let meuBoolean: boolean = false;
 let minhaPLista: number[] = [1,23,3,19];
 let minhaSLista: Array<string> = ["Luis", "Amilcar", "Tavares"];

 console.log(minhaPLista);
 console.log(minhaSLista);